
public class Student extends User {
	
	String StudentNumber;
	String StudentLessonClass;
	String StudentFirstName;
	String StudentLastName;
	String StudentInstructor;
	
	
	public String getStudentNumber() {
		return StudentNumber;
	}
	public void setStudentNumber(String studentNumber) {
		StudentNumber = studentNumber;
	}
	public String getStudentLessonClass() {
		return StudentLessonClass;
	}
	public void setStudentLessonClass(String studentLessonClass) {
		StudentLessonClass = studentLessonClass;
	}
	public String getStudentFirstName() {
		return StudentFirstName;
	}
	public void setStudentFirstName(String studentFirstName) {
		StudentFirstName = studentFirstName;
	}
	public String getStudentLastName() {
		return StudentLastName;
	}
	public void setStudentLastName(String studentLastName) {
		StudentLastName = studentLastName;
	}
	public String getStudentInstructor() {
		return StudentInstructor;
	}
	public void setStudentInstructor(String studentInstructor) {
		StudentInstructor = studentInstructor;
	}
	
	

}
