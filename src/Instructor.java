
public class Instructor extends User {
	
	String InstructorFirstName;
	String InstructorLastName;
	String InstructorLesson;
	String InstructorClass;
	
	
	public String getInstructorFirstName() {
		return InstructorFirstName;
	}
	public void setInstructorFirstName(String instructorFirstName) {
		InstructorFirstName = instructorFirstName;
	}
	public String getInstructorLastName() {
		return InstructorLastName;
	}
	public void setInstructorLastName(String instructorLastName) {
		InstructorLastName = instructorLastName;
	}
	public String getInstructorLesson() {
		return InstructorLesson;
	}
	public void setInstructorLesson(String instructorLesson) {
		InstructorLesson = instructorLesson;
	}
	public String getInstructorClass() {
		return InstructorClass;
	}
	public void setInstructorClass(String instructorClass) {
		InstructorClass = instructorClass;
	}
	

}
